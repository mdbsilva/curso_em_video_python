# Exercício Python #003 - Somando dois números - Aula 00 até 06 - Mundo 1
# Crie um script Python que leia dois números e tente mostrar a soma entre eles.

# Tarefa 1: Ler dois números
primeiro_numero = float(input('Digite o primeiro valor: '))
segundo_numero = float(input('Digite o segundo valor: '))
soma = primeiro_numero + segundo_numero

# Tarefa 2: Mostrar a soma entre eles
print('A soma entre, ', primeiro_numero, ' e ', segundo_numero, ' é igual a ', soma, '!', sep='')